export function getStaticTranslation(key: string, language: Language): string {
  return translations[key]?.[language] || translations[key]?.['en'] || key;
}

export function getTranslation(key: string, language: Language): string {
  // First try to get the translation from our static translations
  const staticTranslation = translations[key]?.[language] || translations[key]?.['en'] || key;
  
  // If the language is English or we don't have a translation, return the static translation
  if (language === 'en' || !translations[key]) {
    return staticTranslation;
  }

  // For other languages, try to get from cache first
  return getCachedTranslation(staticTranslation, 'en', language);
}

// This function should be called in a useEffect hook to handle async translations
export async function loadTranslation(key: string, language: Language): Promise<string> {
  const staticTranslation = translations[key]?.[language] || translations[key]?.['en'] || key;
  
  if (language === 'en' || !translations[key]) {
    return staticTranslation;
  }

  try {
    const translation = await translateText(staticTranslation, 'en', language);
    setCachedTranslation(staticTranslation, 'en', language, translation);
    return translation;
  } catch (error) {
    console.error('Translation error:', error);
    return staticTranslation;
  }
} 